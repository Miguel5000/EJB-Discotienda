package co.edu.ucundinamarca.ejbdiscotienda.dto;

import co.edu.ucundinamarca.ejbdiscotienda.dto.manager.UsuarioDtoManager;
import co.edu.ucundinamarca.ejbdiscotienda.entity.Usuario;
import java.util.List;

/**
 *
 * @author Miguel
 */
public class RolDto {
    
    private Integer id;
    private String nombre;
    private List<UsuarioDto> usuarios;

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the usuarios
     */
    public List<UsuarioDto> getUsuarios() {
        return usuarios;
    }

    /**
     * @param usuarios the usuarios to set
     */
    public void setUsuarios(List<Usuario> usuarios) {
        this.usuarios = UsuarioDtoManager.convertir(usuarios);
    }
    
    
    
}
